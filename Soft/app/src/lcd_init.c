#include "lcd_init.h"



void LCD_GPIO_Init(void)
{
	LCD_CS_Set();
	LCD_RES_Set();
	LCD_DC_Set();
	LCD_BLK_Set();
}

void LCD_WR_DATA8(uint8_t dat)
{
	LCD_CS_Clr();
	HAL_SPI_Transmit(&hspi1, &dat, 1, 1000);
	LCD_CS_Set();
}

void LCD_WR_DATA(uint16_t dat)
{
	LCD_CS_Clr();
	uint8_t Data[2] = {(dat >> 8), (uint8_t)dat};
	HAL_SPI_Transmit(&hspi1, Data, 2, 1000);
	LCD_CS_Set();
}

void LCD_WR_REG(uint8_t dat)
{
	LCD_DC_Clr();
	LCD_WR_DATA8(dat);
	LCD_DC_Set();
}



/******************************************************************************
      函数说明：设置起始和结束地址
      入口数据：x1,x2 设置列的起始和结束地址
                y1,y2 设置行的起始和结束地址
      返回值：  无
******************************************************************************/
void LCD_Address_Set(uint16_t x1,uint16_t y1,uint16_t x2,uint16_t y2)
{
	if(USE_HORIZONTAL==0)
	{
		LCD_WR_REG(0x2a);//列地址设置
		LCD_WR_DATA(x1);
		LCD_WR_DATA(x2);
		LCD_WR_REG(0x2b);//行地址设置
		LCD_WR_DATA(y1);
		LCD_WR_DATA(y2);
		LCD_WR_REG(0x2c);//储存器写
	}
	else if(USE_HORIZONTAL==1)
	{
		LCD_WR_REG(0x2a);//列地址设置
		LCD_WR_DATA(x1);
		LCD_WR_DATA(x2);
		LCD_WR_REG(0x2b);//行地址设置
		LCD_WR_DATA(y1+80);
		LCD_WR_DATA(y2+80);
		LCD_WR_REG(0x2c);//储存器写
	}
	else if(USE_HORIZONTAL==2)
	{
		LCD_WR_REG(0x2a);//列地址设置
		LCD_WR_DATA(x1);
		LCD_WR_DATA(x2);
		LCD_WR_REG(0x2b);//行地址设置
		LCD_WR_DATA(y1);
		LCD_WR_DATA(y2);
		LCD_WR_REG(0x2c);//储存器写
	}
	else
	{
		LCD_WR_REG(0x2a);//列地址设置
		LCD_WR_DATA(x1+80);
		LCD_WR_DATA(x2+80);
		LCD_WR_REG(0x2b);//行地址设置
		LCD_WR_DATA(y1);
		LCD_WR_DATA(y2);
		LCD_WR_REG(0x2c);//储存器写
	}
}

